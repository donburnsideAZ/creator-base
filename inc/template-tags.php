/**
 * Display social links
 */
function creator_base_social_links() {
    $platforms = array(
        'youtube', 'twitter', 'instagram', 'tiktok', 'facebook',
        'linkedin', 'pinterest', 'bluesky', 'threads', 'patreon', 'spotify', 'apple'
    );
    
    $has_links = false;
    $output = '<div class="footer-social">';
    
    foreach ($platforms as $platform) {
        $url = get_theme_mod('creator_base_social_' . $platform, '');
        
        if ($url) {
            $has_links = true;
            $icon = creator_base_get_social_icon($platform);
            $label = ucfirst($platform);
            if ($platform === 'apple') {
                $label = 'Apple Podcasts';
            }
            
            $output .= '<a href="' . esc_url($url) . '" target="_blank" rel="noopener noreferrer" aria-label="' . esc_attr($label) . '">' . $icon . '</a>';
        }
    }
    
    $output .= '</div>';
    
    if ($has_links) {
        echo $output;
    }
}

