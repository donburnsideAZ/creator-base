/**
 * Display posted on date
 */
function creator_base_posted_on() {
    $time_string = '<time class="entry-date published" datetime="%1$s">%2$s</time>';

    echo '<span class="posted-on">' . sprintf(
        $time_string,
        esc_attr(get_the_date(DATE_W3C)),
        esc_html(get_the_date())
    ) . '</span>';
}

/**
 * Display posted by author
 */
function creator_base_posted_by() {
    echo '<span class="byline">' . esc_html__('by', 'creator-base') . ' <a href="' . esc_url(get_author_posts_url(get_the_author_meta('ID'))) . '">' . esc_html(get_the_author()) . '</a></span>';
}

/**
 * Display category badge
 */
function creator_base_category_badge($post_id = null) {
    if (!$post_id) {
        $post_id = get_the_ID();
    }
    
    $categories = get_the_category($post_id);
    
    if (!empty($categories)) {
        echo '<span class="category-badge"><a href="' . esc_url(get_category_link($categories[0]->term_id)) . '">' . esc_html($categories[0]->name) . '</a></span>';
    }
}

/**
 * Get copyright text
 */
function creator_base_copyright() {
    $custom_text = get_theme_mod('creator_base_copyright', '');
    
    if ($custom_text) {
        $text = str_replace('%year%', date('Y'), $custom_text);
    } else {
        $text = sprintf(
            '&copy; %s %s. %s',
            date('Y'),
            get_bloginfo('name'),
            __('All rights reserved.', 'creator-base')
        );
    }
    
    return $text;
}

